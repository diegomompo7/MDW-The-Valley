// Operador ||
console.log(false || true) //true
console.log(true || false) //true
console.log(true ||  "string") //true
console.log("string" || "string") //string

// Operador &&
console.log(false && true) //true
console.log(true && false) //true
console.log(true &&  "string") //true
console.log("string" && "string") //string